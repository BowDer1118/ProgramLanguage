#include <stdio.h>

int main(){
    int n;
    int count;
    int temp;
    while(scanf("%d",&n)!=EOF){
        temp=1;
        count=0;
        while(temp<=n){
            temp*=2;
            count++;
        }
        printf("%d\n",count);
    }
    return 0;
}


